# Lê o primeiro número e converte para float
num01 = float(input("Digite o primeiro número: "))

# Lê o segundo número e converte para float
num02 = float(input("Digite o segundo número: "))

# Operações matemáticas básicas
adição = num01 + num02          # Soma
subtração = num01 - num02       # Subtração
multiplicação = num01 * num02   # Multiplicação
divisao = num01 / num02         # Divisão
exponenciacao = num01 ** num02  # Potência

# Exibe os resultados usando f-strings
print(f"Adição = {adição}\n")
print(f"Subtração = {subtração}\n")
print(f"Multiplicação = {multiplicação}\n")
print(f"Divisão = {divisao}\n")
print(f"Exponenciação = {exponenciacao}\n")
