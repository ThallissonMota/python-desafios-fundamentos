# Define uma função chamada quadrado
def quadrado():
    
    # Solicita um número ao usuário e converte para inteiro
    x = int(input("Digite um número: "))
    
    # Calcula o quadrado do número
    raiz = x ** 2

    # Exibe o resultado
    print(f"A raiz quadrada de {x} é {raiz}")

# Chamada da função para executar o código
quadrado()
